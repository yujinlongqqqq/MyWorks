package com.ypyg.shopmanager.bean;

public class GoodCategoryBean {
	private String id;
	private String name;
	private int smallLogo;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getSmallLogo() {
		return smallLogo;
	}

	public void setSmallLogo(int smallLogo) {
		this.smallLogo = smallLogo;
	}

}
